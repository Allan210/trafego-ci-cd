CREATE SEQUENCE SEQ_TRAFEGOS
    START WITH 1
    INCREMENT BY 1
    NOCACHE
    NOCYCLE;
CREATE TABLE TBL_TRAFEGOS(
    ID INTEGER DEFAULT SEQ_TRAFEGOS.NEXTVAL NOT NULL,
    ESTADO VARCHAR2(100) NOT NULL,
    CIDADE VARCHAR2(100) NOT NULL,
    SITUACAO VARCHAR2(100) NOT NULL,
    DATA_OCORRIDO DATE);